const fs = require("fs");
const path = require("path");
const os = require("os");

const projectRoot = path.resolve(__dirname, "..");
const localPropertiesPath = path.join(projectRoot, "android", "local.properties");

const androidDir = path.join(projectRoot, "android");
if (!fs.existsSync(androidDir)) {
  process.exit(0);
}

let sdkDir =
  process.env.ANDROID_HOME ||
  process.env.ANDROID_SDK_ROOT ||
  path.join(os.homedir(), "Library", "Android", "sdk");

sdkDir = sdkDir.replace(/^~/, os.homedir());

let shouldWrite = true;
if (fs.existsSync(localPropertiesPath)) {
  const content = fs.readFileSync(localPropertiesPath, "utf8");
  const match = content.match(/sdk\.dir=(.+)/);
  if (match && match[1].trim() && fs.existsSync(match[1].trim())) {
    shouldWrite = false;
  }
}

if (shouldWrite && fs.existsSync(sdkDir)) {
  const content = `## This file must *NOT* be checked into Version Control Systems,
# as it contains information specific to your local configuration.
#
# Location of the SDK. This is only used by Gradle.
# Generated by scripts/ensure-android-sdk.js

sdk.dir=${sdkDir.replace(/\\/g, "/")}
`;
  fs.writeFileSync(localPropertiesPath, content, "utf8");
  console.log("Written android/local.properties with sdk.dir=" + sdkDir);
} else if (shouldWrite && !fs.existsSync(sdkDir)) {
  console.warn(
    "Android SDK not found at " +
      sdkDir +
      ". Set ANDROID_HOME or install Android Studio."
  );
}
